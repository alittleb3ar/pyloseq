"""Pure-Python loaders for the R phyloseq golden-file reference outputs.

Golden files are generated by ``scripts/generate_golden.R`` and committed to
``tests/golden/`` via Git LFS.  These loaders have no runtime dependency on R
or QIIME 2 — they only need pandas and pyarrow.

"""

from __future__ import annotations

import hashlib
import json
from pathlib import Path
from typing import Any

import pandas as pd

# Resolve golden dir relative to this file: src/pyloseq/testing/ -> repo_root/tests/golden/
_GOLDEN_DIR = Path(__file__).parent.parent.parent.parent / "tests" / "golden"


def _require_golden() -> None:
    if not _GOLDEN_DIR.exists():
        raise FileNotFoundError(
            f"Golden directory not found: {_GOLDEN_DIR}\n"
            "Run `Rscript scripts/generate_golden.R` to generate reference files."
        )


def _load_parquet_as_series(path: Path) -> pd.Series:
    """Load a two-column parquet (``__index__``, ``value``) as a named Series."""
    df = pd.read_parquet(path)
    return pd.Series(df["value"].values, index=df["__index__"], name=path.stem)


def _load_parquet_as_frame(path: Path) -> pd.DataFrame:
    """Load a parquet that stores rownames in ``__index__``."""
    df = pd.read_parquet(path)
    if "__index__" in df.columns:
        df = df.set_index("__index__")
        df.index.name = None
    return df


def _load_dataset(name: str) -> dict[str, Any]:
    _require_golden()
    base = _GOLDEN_DIR / name
    if not base.is_dir():
        raise FileNotFoundError(
            f"No golden files for dataset '{name}' at {base}\n"
            "Run `Rscript scripts/generate_golden.R`."
        )

    result: dict[str, Any] = {}

    for key, loader in [
        ("otu_table", _load_parquet_as_frame),
        ("sample_data", _load_parquet_as_frame),
        ("tax_table", _load_parquet_as_frame),
        ("taxa_sums", _load_parquet_as_series),
        ("sample_sums", _load_parquet_as_series),
    ]:
        p = base / f"{key}.parquet"
        if p.exists():
            result[key] = loader(p)

    nwk = base / "phy_tree.nwk"
    if nwk.exists():
        result["phy_tree_newick"] = nwk.read_text()

    fa = base / "refseq.fasta"
    if fa.exists():
        result["refseq_path"] = fa

    return result


def load_global_patterns_reference() -> dict[str, Any]:
    """Return the GlobalPatterns reference data as a dict of DataFrames / Series.

    Keys: ``otu_table``, ``sample_data``, ``tax_table``, ``taxa_sums``,
    ``sample_sums``, ``phy_tree_newick``.

    R reference: ``data("GlobalPatterns"); phyloseq::GlobalPatterns``
    """
    return _load_dataset("GlobalPatterns")


def load_enterotype_reference() -> dict[str, Any]:
    """Return the enterotype reference data.

    R reference: ``data("enterotype"); phyloseq::enterotype``
    """
    return _load_dataset("enterotype")


def load_esophagus_reference() -> dict[str, Any]:
    """Return the esophagus reference data.

    R reference: ``data("esophagus"); phyloseq::esophagus``
    """
    return _load_dataset("esophagus")


def load_soilrep_reference() -> dict[str, Any]:
    """Return the soilrep reference data.

    R reference: ``data("soilrep"); phyloseq::soilrep``
    """
    return _load_dataset("soilrep")


def load_golden(dataset: str, function: str, **params: Any) -> Any:
    """Load a golden output file for an analysis function.

    Parameters
    ----------
    dataset:
        One of ``"GlobalPatterns"``, ``"enterotype"``, ``"esophagus"``,
        ``"soilrep"``.
    function:
        Name of the R phyloseq function, e.g. ``"estimate_richness"``.
    **params:
        Parameters passed to the function — used to compute the file path hash.
        Must match the params used when ``generate_golden.R`` was run.

    Returns
    -------
    pd.DataFrame
        The golden output, with the original rownames restored as the index.

    Raises
    ------
    FileNotFoundError
        If the golden file does not exist for the given dataset/function/params.
    """
    _require_golden()
    # Empty params → stable "default.parquet" name to avoid cross-language hash divergence.
    # Non-empty params → MD5 of the JSON-serialized params dict.
    if not params:
        filename = "default.parquet"
    else:
        filename = (
            hashlib.md5(json.dumps(params, sort_keys=True).encode()).hexdigest()[:8]
            + ".parquet"
        )
    path = _GOLDEN_DIR / dataset / function / filename
    if not path.exists():
        raise FileNotFoundError(
            f"No golden file at {path}\n"
            f"Run `Rscript scripts/generate_golden.R` and ensure params match: {params}"
        )
    return _load_parquet_as_frame(path)


def golden_provenance() -> dict[str, str]:
    """Return the R version provenance recorded when golden files were generated."""
    _require_golden()
    prov_path = _GOLDEN_DIR / "PROVENANCE.json"
    if not prov_path.exists():
        raise FileNotFoundError(
            f"PROVENANCE.json not found at {prov_path}. Run generate_golden.R."
        )
    with prov_path.open() as f:
        return json.load(f)  # type: ignore[no-any-return]
